package day03_Variables;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Hello everyone");
        System.out.println("My name is cybertek, I am from DC");
        System.out.println("");
        System.out.println("Hello everyone\nMy name is cybertek, I am from DC");

//t:
        System.out.println("\tToday I learned Java");
        System.out.println("\tit was so much fun");
        System.out.println("============================");

        System.out.println("/"); // prints single front slash
        System.out.println("\\"); // prints single backslash

        System.out.println("my name is 'Javid'");
        System.out.println("my name is \'Javid\'");
        System.out.println("");
        System.out.println("Hello this is \"Titanic\"");

    }
}

