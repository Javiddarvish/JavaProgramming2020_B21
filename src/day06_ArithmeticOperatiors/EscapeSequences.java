public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("\tHello Everyone\n\t\tHello Batch 21\nToday I learned new 'programming' language \"Java\"");
        System.out.println("=============================================");

        System.out.println("My favorite code is:\n\t\"Winter is Coming\"\n\t\t\t\t- John Snow");

        System.out.println("\\");
        System.out.println("==============================================");




    }
}

/*
 Escape sequences: for text
        \n: append new line
        \t: tab space, paragraph space
        \\: \
        \": double quote

        Hello Everyone
        Hello Batch 21
        Today I learned "Java"
 */